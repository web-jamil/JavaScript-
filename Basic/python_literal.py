# Integer Literals
decimal_number = 100  # Decimal integer
binary_number = 0b1010  # Binary literal, equivalent to 10 in decimal
octal_number = 0o12  # Octal literal, equivalent to 10 in decimal
hexadecimal_number = 0xA  # Hexadecimal literal, equivalent to 10 in decimal

# Floating-Point Literals
pi = 3.14159  # A regular floating-point number
scientific_number = 1.5e3  # Scientific notation, equivalent to 1500.0
negative_scientific = 2.5e-2  # Scientific notation, equivalent to 0.025

# Complex Number Literals
complex_number = 3 + 4j  # Complex number with real part 3 and imaginary part 4

# String Literals
single_quoted = 'Hello, World!'  # Single-quoted string
double_quoted = "Python is fun"  # Double-quoted string
multiline_string = """This is a 
multi-line string in Python"""  # Triple-quoted multi-line string

# Raw String Literals (used to prevent escape characters)
file_path = r"C:\Users\Name\Documents"  # Raw string for file paths

# Boolean Literals
is_active = True  # Boolean literal True
is_disabled = False  # Boolean literal False

# None Literal (representing null values)
user = None  # None represents the absence of a value

# List Literals
fruits = ["apple", "banana", "cherry"]  # List of strings
numbers = [1, 2, 3, 4]  # List of integers

# Tuple Literals
coordinates = (10, 20)  # Tuple with two elements
single_element_tuple = (5,)  # Tuple with one element, comma is required

# Set Literals
unique_numbers = {1, 2, 3, 4}  # Set of numbers, no duplicates allowed
fruits_set = {"apple", "banana", "cherry"}  # Set of fruits

# Dictionary Literals
person = {"name": "Alice", "age": 25, "city": "New York"}  # Dictionary with key-value pairs

# Formatted String Literals (f-strings)
name = "Alice"
age = 25
greeting = f"Hello, {name}! You are {age} years old."  # f-string for formatting strings
